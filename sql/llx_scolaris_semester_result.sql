CREATE TABLE llx_scolaris_semester_result (
  rowid integer AUTO_INCREMENT PRIMARY KEY,
  entity integer NOT NULL DEFAULT 1,
  fk_student integer NOT NULL,
  fk_enrollment integer NOT NULL,
  fk_semester integer NOT NULL,
  fk_rule_profile integer NULL,
  attempt_number smallint NOT NULL DEFAULT 1,
  total_coefficients decimal(10,2) NOT NULL DEFAULT 0.00,
  weighted_total decimal(20,8) NOT NULL DEFAULT 0.00000000,
  semester_average decimal(10,4) NOT NULL DEFAULT 0.0000,
  credits_target decimal(10,2) NOT NULL DEFAULT 0.00,
  credits_earned decimal(10,2) NOT NULL DEFAULT 0.00,
  decision_code varchar(32) NULL,
  mention_code varchar(32) NULL,
  rank_value integer NULL,
  is_published tinyint NOT NULL DEFAULT 0,
  status smallint NOT NULL DEFAULT 0,
  calculation_details longtext NULL,
  calculated_at datetime NULL,
  published_at datetime NULL,
  locked_at datetime NULL,
  tms timestamp NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  fk_user_calculated integer NULL,
  fk_user_published integer NULL,
  fk_user_locked integer NULL,
  import_key varchar(14)
) ENGINE=innodb;
